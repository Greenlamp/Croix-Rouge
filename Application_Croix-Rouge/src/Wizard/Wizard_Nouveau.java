    private void BenregistrerActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BenregistrerActionPerformed
        String nom = Gnom.getText();
        String prenom = Gprenom.getText();
        String nomEpoux = GnomEpoux.getText();
        String dateNaissance = Gjour.getSelectedItem().toString() + "/" + Gmois.getSelectedItem().toString() + "/" + Gannee.getSelectedItem().toString();
        String sexe = Gsexe.getSelectedItem().toString();
        String email = Gmail.getText();
        String rue = Grue.getText();
        String numero = Gnumero.getText();
        String ville = Gville.getText();
        String codePostal = GcodePostal.getText();
        String boite = Gboite.getText();
        String nationalite = Gnationalite.getText();

        String[] data = {nom, prenom, nomEpoux, dateNaissance, sexe, email, rue, numero, ville, codePostal, boite, nationalite};
        socket.send(new PacketCom(States.NOUVEAU_VOLONTAIRE, (Object)data));
        try {
            PacketCom packetReponse = socket.receive();
            String type = packetReponse.getType();
            if(type.equals(States.NOUVEAU_VOLONTAIRE_OUI)){
                JOptionPane.showMessageDialog(parent, "Ajout nouveau volontaire réussi.");
                this.dispose();
            }else if(type.equals(States.NOUVEAU_VOLONTAIRE_NON)){
                String message = (String) packetReponse.getObjet();
                JOptionPane.showMessageDialog(parent, message);
            }
        } catch (Exception ex) {
            Logger.getLogger(Wizard_Nouveau.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_BenregistrerActionPerformed

