    private boolean checkChamp() {
        if(!EasyCheck.checkString(Gnom.getText())){
            parent.afficherMessage("Nom invalide");
            Gnom.requestFocus();
            return false;
        }
        return true;
    }

    private void remplirChamps() {
        Gnom.setText(lieu);
    }

    private void enregistrerData() {
        String nom = Gnom.getText();
        PacketCom packet = null;

        if(!edited){
            packet = new PacketCom(States.NEW_LIEU, (Object)nom);
        }else{
            String oldLieu = parent.getLieu();
            String[] data = {oldLieu, nom};
            packet = new PacketCom(States.EDIT_LIEU, (Object)data);
        }
        socket.send(packet);
        try {
            PacketCom retour = socket.receive();
            traitementPacket(retour);
        } catch (Exception ex) {
            Logger.getLogger(M_NewEditLieux.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

    private void traitementPacket(PacketCom retour) {
        String type = retour.getType();
        boolean cont = false;
        if(type.equals(States.NEW_LIEU_OUI)){
            parent.afficherMessage("Nouveau lieu ajouté avec succès");
            cont = true;
        }else if(type.equals(States.NEW_LIEU_NON)){
            String message = (String)retour.getObjet();
            parent.afficherMessage(message);
        }else if(type.equals(States.EDIT_LIEU_OUI)){
            parent.afficherMessage("Lieu modifié avec succès");
            cont = true;
        }else if(type.equals(States.EDIT_LIEU_NON)){
            String message = (String)retour.getObjet();
            parent.afficherMessage(message);
        }else{
            String message = (String)retour.getObjet();
            parent.afficherMessage(message);
        }
        if(cont){
            parent.changeState(Main.LOGGED);
        }

    }
