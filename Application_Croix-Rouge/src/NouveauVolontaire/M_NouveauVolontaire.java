    private void BcontinuerActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BcontinuerActionPerformed
        if(G1.isSelected()){
            parent.changeState(Main.NOUVEAU_VOLONTAIREP1);
        }else if(G2.isSelected()){
            String matricule = Gmatricule.getText();
            if(matricule == null){
                parent.afficherMessage("Veuillez communiquer un matricule");
                return;
            }
            parent.setMatricule(matricule);
            parent.changeState(Main.NOUVEAU_VOLONTAIREP1);
        }else if(G3.isSelected()){
            parent.changeState(Main.NOUVEAU_VOLONTAIREP1);
        }else{
            parent.afficherMessage("Veuillez cocher une case");
        }
    }//GEN-LAST:event_BcontinuerActionPerformed

