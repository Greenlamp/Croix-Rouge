    private void setDate(JComboBox jour, JComboBox mois, JComboBox annee, Date dateObtention) {
        String date = new SimpleDateFormat("dd/MM/yyyy").format(dateObtention);
        String[] split = date.split("/");
        int jourI = Integer.parseInt(split[0]);
        int moisI = Integer.parseInt(split[1]);
        int anneeI = Integer.parseInt(split[2]);
        jour.setSelectedIndex(jourI-1);
        mois.setSelectedIndex(moisI-1);

        boolean found = false;
        for(int i=0; i<annee.getItemCount() && !found; i++){
            if(Integer.parseInt(annee.getItemAt(i).toString()) == anneeI){
                annee.setSelectedIndex(i);
            }
        }
    }

